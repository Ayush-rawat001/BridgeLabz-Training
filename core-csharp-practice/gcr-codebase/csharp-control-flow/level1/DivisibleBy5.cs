using System;

class DivisibleBy5{
    static void Main(){
        int num= Convert.ToInt32(Console.ReadLine());
          if (num % 5 == 0){
            Console.WriteLine("Is the number " + number + " divisible by 5? Yes");
        }
        else{
            Console.WriteLine("Is the number " + number + " divisible by 5? No");
        }
    }
}
